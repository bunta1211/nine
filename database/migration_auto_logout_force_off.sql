-- =====================================================
-- 自動ログアウトを「しない」に全員強制
-- 一度ログインしたら自動ログアウトしない方針に合わせ、
-- 既存ユーザー全員の詳細設定を「自動ログアウトしない」(0) に変更する。
-- 選択肢は「自動ログアウトしない」と「24時間でログアウト」の2つのみ残す。
-- =====================================================

-- 全ユーザーの自動ログアウト時間を 0（しない）に更新
UPDATE user_advanced_settings
SET auto_logout_minutes = 0, updated_at = NOW()
WHERE auto_logout_minutes != 0;
