-- users.phone（携帯電話）カラムの追加
-- 個人を特定する検索で利用。登録・設定画面で入力可能。
-- MySQL 5.7 / MariaDB 10.x でも実行可能な構文です。
-- 実行時は「1つ目」「2つ目」をそれぞれコピーして1文ずつ実行してください。
-- =====================================================

-- ========== 1つ目：カラム追加（これをコピーして実行） ==========
ALTER TABLE users ADD COLUMN phone VARCHAR(20) DEFAULT NULL COMMENT '携帯電話番号（数字のみ）' AFTER password_hash;

-- ========== 2つ目：インデックス追加（これをコピーして実行） ==========
ALTER TABLE users ADD INDEX idx_phone (phone);
